package ex03;

public class MyStar01 {
    public static void main(String[] args) {
        // 1단계 첫줄만 찍어보기(반복문)
        int end = 0;
        end = end + 1;
        for (int i = 0; i < end; i++) {
            System.out.print("*");

        }
        System.out.println();

        // 2단계 두번째줄만 찍어보기(반복문)
        end = end + 1;
        for (int i = 0; i < end; i++) {
            System.out.print("*");

        }
        System.out.println();

        // 3단계 세번째줄만 찍어보기(반복문)
        end = end + 1;
        for (int i = 0; i < end; i++) {
            System.out.print("*");

        }
        System.out.println();

        // 4단계 네번째줄만 찍어보기(반복문)
        end = end + 1;
        for (int i = 0; i < end; i++) {
            System.out.print("*");

        }
        System.out.println();

        // 5단계 다섯번째줄만 찍어보기(반복문)
        end = end + 1;
        for (int i = 0; i < end; i++) {
            System.out.print("*");

        }
        System.out.println();
    }

}
